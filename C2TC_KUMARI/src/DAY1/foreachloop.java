package DAY1;

public class foreachloop {
	public static void main(String[] args) {
		int a[]= {10,20,30,40,50};
		
		for(int i:a)
		{
			System.out.println(i);
		}
		
		char ch[]= {'v','k','l','h'};
		 for(char c:ch)
		 {
			 System.out.println(c);
		 }
		 System.out.println(" ");
		 String s1[]= {"kumari", "is","a","programmer" };
		 for(String s:s1)
		 {
			 System.out.println(s+" ");
		 }
	}

}
